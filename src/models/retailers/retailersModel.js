import mongoose from "mongoose";

const retailersSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const Retailers = mongoose.model("Retailers", retailersSchema);
export default Retailers;
